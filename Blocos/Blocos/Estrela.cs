namespace Blocos
{
    internal class Estrela
    {
        public float x { get; set; }
        public float y { get; set; }
        public float Alpha { get; set; }

        public Estrela(float x, float y, float Alpha)
        {
            this.x = x;
            this.y = y;
            this.Alpha = Alpha;
        }
    }
}
